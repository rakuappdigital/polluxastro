export function getMoonPhase(date: Date = new Date()): typeof MOON_PHASES[0] {
  const known = new Date(2000, 0, 6);
  const msPerDay = 86400000;
  const daysSinceKnown = (date.getTime() - known.getTime()) / msPerDay;
  const lunarCycle = 29.53058867;
  const phase = ((daysSinceKnown % lunarCycle) / lunarCycle) * 8;
  return MOON_PHASES[Math.floor(phase) % 8];
}

