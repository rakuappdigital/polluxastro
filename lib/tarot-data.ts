export const MAJOR_ARCANA = TAROT_CARDS.filter(c => c.arcana === "major");
export const MINOR_ARCANA = TAROT_CARDS.filter(c => c.arcana === "minor");
export const WANDS = TAROT_CARDS.filter(c => c.suit === "wands");
export const CUPS = TAROT_CARDS.filter(c => c.suit === "cups");
export const SWORDS = TAROT_CARDS.filter(c => c.suit === "swords");
export const PENTACLES = TAROT_CARDS.filter(c => c.suit === "pentacles");

export function getCardById(id: number): TarotCard | undefined {
  return TAROT_CARDS.find(c => c.id === id);
}

export function getRandomCard(): TarotCard {
  return TAROT_CARDS[Math.floor(Math.random() * TAROT_CARDS.length)];
}

export function getRandomCards(count: number, excludeIds: number[] = []): TarotCard[] {
  const pool = TAROT_CARDS.filter(c => !excludeIds.includes(c.id));
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getDailyCard(date: Date = new Date()): TarotCard {
  const dayOfYear = Math.floor(
    (date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86400000
  );
  const index = dayOfYear % TAROT_CARDS.length;
  return TAROT_CARDS[index];
}

export function calculateBirthCards(birthDate: Date): { primary: TarotCard; secondary?: TarotCard } {
  const day = birthDate.getDate();
  const month = birthDate.getMonth() + 1;
  const year = birthDate.getFullYear();

  let sum = day + month + year;
  while (sum > 22) {
    sum = String(sum).split("").reduce((a, b) => a + parseInt(b), 0);
  }

  const primaryId = sum === 22 ? 0 : sum - 1;
  const primary = MAJOR_ARCANA[primaryId] || MAJOR_ARCANA[0];

  let secondary: TarotCard | undefined;
  if (sum > 9) {
    const secSum = String(sum).split("").reduce((a, b) => a + parseInt(b), 0);
    if (secSum !== sum) {
      secondary = MAJOR_ARCANA[secSum - 1];
    }
  }

  return { primary, secondary };
}

